    /**
     * Get the array representation of the notification for web push.
     *
     * @return array<string, mixed>
     */
    public function toWebPush($notifiable, $notification)
    {
        $userName = $notifiable->name ?? 'Valued Member'; // Fallback if name is not available

        return (new WebPushMessage)
            ->title("New Booking, {$userName}!")
            ->body("You have a new booking. Check your schedule for details.")
            ->action('View Booking', url('/bookings'))
            ->icon(asset('{{ asset("public/img/Logo.jpg") }}')) ;// Assuming your logo is in public/img/Logo.jpg
    }

